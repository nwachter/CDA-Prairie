// Dessiner la carte
function drawMap(): void {
  const mapDiv = document.getElementById("map");
  if (mapDiv) {
    mapDiv.innerHTML = "";

    for (let y = 0; y < 32; y++) {
      for (let x = 0; x < 40; x++) {
        const cell = document.createElement("div");
        cell.className = `cell color-${map[y][x]}`;
        cell.onclick = () => changeCell(x, y);
        mapDiv.appendChild(cell);
      }
    }
  }
}

// Changer une cellule
function changeCell(x: number, y: number): void {
  const colorSelect = document.getElementById("colorSelect") as HTMLSelectElement;
  if (colorSelect) {
    const color: number = parseInt(colorSelect.value);
    map[y][x] = color;
    drawMap();
  }
}

// Générer aléatoire
function randomize(): void {
  for (let y = 0; y < 32; y++) {
    for (let x = 0; x < 40; x++) {
      map[y][x] = Math.floor(Math.random() * 5) + 1;
    }
  }
  drawMap();
}

